    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String name = nameField.getText().toUpperCase();
        String priceString = priceField.getText();
        Double price = Double.parseDouble(priceString);
        String qtyString = qtyField.getText();
        int qty = Integer.parseInt(qtyString);
        String type = typeBox.getSelectedItem().toString();
        
        String insertItem = "INSERT INTO MEDICAL (DESCRIPTION, AMOUNT, QUANTITY, TYPE) VALUES (?,?,?,?)";
        try {
            pst = conn.prepareStatement(insertItem);
            int col = 1;
            pst.setString(col++, name);
            pst.setDouble(col++, price);
            pst.setInt(col++, qty);
            pst.setString(col++, type);
            pst.execute();
            JOptionPane.showMessageDialog(null, qtyString+" "+name+" have been added to the database");
            populateTable();
        } catch (SQLException ex) {
            Logger.getLogger(AddMedical.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

