    private void populateTable(){
        String populateQuery = "SELECT * FROM MEDICAL ORDER BY DESCRIPTION ASC";
        try {
            pst = conn.prepareStatement(populateQuery);
            rs = pst.executeQuery();
            while(rs.next()){
                String item = rs.getString("description");
                Double amount = rs.getDouble("amount");
                String type = rs.getString("Type");
                int quantity = rs.getInt("quantity");
                addItemTableModel.addRow(new Object[] {item, amount, quantity, type});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Medical.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
